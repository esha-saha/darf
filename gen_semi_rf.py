alphas = (1. - torch.linspace(0.0001, 0.02, n_steps)).to(device)
alphas_cumprod = torch.cumprod(alphas, axis=0).to(device)

sqrt_alphas_cumprod = torch.sqrt(alphas_cumprod).to(device)
sqrt_one_minus_alphas_cumprod = torch.sqrt(1 - sqrt_alphas_cumprod ** 2).to(device)

def q_sample(x_0, t, noise):
    """
    Sample x at time t given the value of x at t=0 and the noise
    """
    return sqrt_alphas_cumprod.gather(-1, t).repeat(d,1).T * x_0 + sqrt_one_minus_alphas_cumprod.gather(-1, t).repeat(d,1).T  * noise

