alphas_cumprod_prev = F.pad(alphas_cumprod[:-1], (1, 0), value=1.0)
posterior_variance = ((1 - alphas) * (1. - alphas_cumprod_prev) / (1. - alphas_cumprod))

def p_sample(x, t):
    """
    One step of revese process sampling - Algorithm 2 from the paper
    """
    if t!=0:
      alpha_t = alphas.cpu().gather(-1, t.cpu()).repeat(d,1).T
      sqrt_one_minus_alphas_cumprod_t = sqrt_one_minus_alphas_cumprod.cpu().gather(-1, t.cpu()).repeat(d,1).T
      # Get mean x[t - 1] conditioned at x[t] - see eq. (11) in the paper
      model_mean = torch.sqrt(1.0 / alpha_t) * (x - (1 - alpha_t) * denoise(x.float().to(device), t.reshape(-1,1).to(device)).cpu() / sqrt_one_minus_alphas_cumprod_t)
      # Get variance of x[t - 1]
      model_var = posterior_variance.cpu().gather(-1, t.cpu()).repeat(d,1).T
      # Samples for the normal distribution with given mean and variance
    else:
      model_var = 0
    return model_mean + torch.sqrt(model_var) * torch.randn(d)



